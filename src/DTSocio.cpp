#include "DTSocio.h"

DTSocio::DTSocio(){}

DTSocio::~DTSocio(){}

// Setters.
void DTSocio::setCi(string ci) {

    this->ci = ci;
}
void DTSocio::setNombre(string nombre) {

    this->nombre = nombre;
}


// Getters.
string DTSocio::getCI() {

    return this->ci;
};

string DTSocio::getNombre() {

    return this->nombre;
};







